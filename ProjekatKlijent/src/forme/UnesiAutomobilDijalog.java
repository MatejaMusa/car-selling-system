    private void btnUnesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiActionPerformed
        String marka=txtMarka.getText();
        int godiste=(int) cmbGodiste.getSelectedItem();
        TipAutomobila tip=(TipAutomobila) cmbTip.getSelectedItem();
        boolean dodaci=ckDodatak.isSelected();
        boolean polovan=ckPolovno.isSelected();
        if(txtCena.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Polje cene ne sme biti prazno");
        }else{
            double cenaBezPDV=Double.parseDouble(txtCena.getText());
            try {
                 FileInputStream in = new FileInputStream("pdv.properties");
                 Properties props = new Properties();
                 props.load(in);
                 String PDV = props.getProperty("PDV");
                 double pdvIznos=Double.parseDouble(PDV);
                 double cenaSaPDV=(((100*pdvIznos)/(100+pdvIznos))*100)+cenaBezPDV;
                 cenaSaPDV=Math.round(cenaSaPDV);
                 Automobil a= new Automobil(-1, marka, godiste, tip, dodaci, polovan, cenaBezPDV, cenaSaPDV);
            
                 a=KontrolerKlijent.getInstance().unesiAutomobil(a);
                 if(a.getAutomobilID()!=-1){
                    JOptionPane.showMessageDialog(this, "Uspesno unet automobil");
                    parent.osvezi();
                    this.dispose();
                }else{
                JOptionPane.showMessageDialog(this, "Nije unet automobil");
                }
            
            
            } catch (FileNotFoundException ex) {
            Logger.getLogger(UnesiAutomobilDijalog.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
            Logger.getLogger(UnesiAutomobilDijalog.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
            Logger.getLogger(UnesiAutomobilDijalog.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Auto postoji u bazi");
            }
        }
   
    }//GEN-LAST:event_btnUnesiActionPerformed

