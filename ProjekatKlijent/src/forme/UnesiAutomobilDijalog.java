    private void popuniKomboBox() {
        cmbGodiste.removeAllItems();
        cmbTip.removeAllItems();
        
        for (Enum tip : TipAutomobila.values()) {
            cmbTip.addItem(tip);
        }
        
        for (int i = 2000; i <= new GregorianCalendar().get(GregorianCalendar.YEAR); i++) {
            cmbGodiste.addItem(i);
        }
    }
